package operacoes;

public class Max {
    public static void main(String[] args) {
        int x = 100;
        int y = 120;
        /*A operação Max, faz o oposto da operação Min
         * pega 2 valores e devolve o maior deles*/
        int z = Math.max(x, y);

        System.out.println(z);

    }
}
