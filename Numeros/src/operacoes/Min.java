package operacoes;

public class Min {
    public static void main(String[] args) {
        int x = 100;
        int y = 120;
        /*A operação mim, como o nome diz é uma operação matemática que devolve
         * o menor número de 2 variaveis, nesse caso é o 100*/
        int z = Math.min(x, y);

        System.out.println(z);
    }
}
